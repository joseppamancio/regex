import re

string = '''1,2,3,4,5,6,a.b c!d?e	-
f_g'''


# ================ DIGITOS - [0-9] ================ #
print(re.findall(r'\d', string))
# ['1', '2', '3', '4', '5', '6']


# ================ NÃO DIGITOS - [^0-9] ================ #
print(re.findall(r'\D', string))
# [',', ',', ',', ',', ',', ',', 'a', '.', 'b', ' ', 'c', '!', 'd', '?', 'e', '\t', '-', '\n', 'f', '_', 'g']


# ================ CARACTERES - [a-zA-Z0-9_] ================ #
print(re.findall(r'\w', string))
# ['1', '2', '3', '4', '5', '6', 'a', 'b', 'c', 'd', 'e', 'f', '_', 'g']


# ================ NÃO CARACTERES - [^a-zA-Z0-9_] ================ #
print(re.findall(r'\W', string))
# [',', ',', ',', ',', ',', ',', '.', ' ', '!', '?', '\t', '-', '\n']


# ================ ESPAÇOS - [ \t\n\r\f] ================ #
print(re.findall(r'\s', string))
# [' ', '\t', '\n']


# ================ NÃO ESPAÇOS - [^ \t\n\r\f] ================ #
print(re.findall(r'\S', string))
# ['1', ',', '2', ',', '3', ',', '4', ',', '5', ',', '6', ',', 'a', '.', 'b', 'c', '!', 'd', '?', 'e', '-', 'f', '_', 'g']
